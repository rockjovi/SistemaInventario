    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        
            if (this.txtCodigo.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar Codigo", "Error de ingreso", JOptionPane.ERROR_MESSAGE);
            this.txtCodigo.requestFocus();
            return;
        }
            if (this.txtNombre.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar Nombre", "Error de ingreso", JOptionPane.ERROR_MESSAGE);
            this.txtNombre.requestFocus();
            return;
        }
             if (this.txtFactura.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar Factura", "Error de ingreso", JOptionPane.ERROR_MESSAGE);
            this.txtFactura.requestFocus();
            return;
        }
            if (this.txtFecha.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar Fecha", "Error de ingreso", JOptionPane.ERROR_MESSAGE);
            this.txtFecha.requestFocus();
            return;
        }
            if (this.txtProveedor.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar Proveedor", "Error de ingreso", JOptionPane.ERROR_MESSAGE);
            this.txtProveedor.requestFocus();
            return;
        }
            Ingresar();
            Mostrar();
    }//GEN-LAST:event_btnIngresarActionPerformed

